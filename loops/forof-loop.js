// For of loop in JavaScript
// It is used to iterate over the values of an iterable object like an array, string, etc.
// for-of loop can also be used with maps and sets.

const arr = [1,2,3,4,5];

for (const num of arr) {
    console.log(num);
}

const greetings = 'Hello World!';
for (const char of greetings) {
    console.log(char);
}
